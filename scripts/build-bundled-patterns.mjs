#!/usr/bin/env node
// Generate src/patterns/bundled-data.ts from patterns/*.md at the repo
// root. Hooked into the prebuild so the bundled fallback for
// `htmlbin patterns init` always matches what's published at
// https://htmlbin.dev/.well-known/patterns/.
//
// If the patterns/ dir is missing, we still emit a valid file with an
// empty record so the CLI ships rather than fails the build.

import { readdir, readFile, writeFile, mkdir } from "node:fs/promises";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..");
const patternsDir = join(repoRoot, "patterns");
const outFile = join(repoRoot, "src/patterns/bundled-data.ts");

let entries = [];
try {
  const files = (await readdir(patternsDir)).filter((f) => f.endsWith(".md")).sort();
  for (const f of files) {
    const name = f.replace(/\.md$/, "");
    const body = await readFile(join(patternsDir, f), "utf8");
    entries.push({ name, body });
  }
} catch (e) {
  if (e?.code !== "ENOENT") throw e;
  console.warn(
    `[bundled-patterns] patterns/ not found at ${patternsDir} — emitting empty bundle`
  );
}

const lines = [
  "// Generated by scripts/build-bundled-patterns.mjs — do not edit.",
  "// Source: patterns/*.md at the repo root.",
  "",
  "export const BUNDLED_PATTERNS: ReadonlyArray<{ name: string; body: string }> = [",
];
for (const { name, body } of entries) {
  // String literal with backslash + backtick + ${} escaped.
  const safe = body
    .replace(/\\/g, "\\\\")
    .replace(/`/g, "\\`")
    .replace(/\$\{/g, "\\${");
  lines.push(`  {`);
  lines.push(`    name: ${JSON.stringify(name)},`);
  lines.push(`    body: \`${safe}\`,`);
  lines.push(`  },`);
}
lines.push("];");
lines.push("");

await mkdir(dirname(outFile), { recursive: true });
await writeFile(outFile, lines.join("\n"), "utf8");
console.log(
  `[bundled-patterns] wrote ${entries.length} pattern(s) → ${outFile.replace(repoRoot + "/", "")}`
);
